const { chromium } = require('playwright');
(async () => {
  const browser = await chromium.launch();
  const page = await browser.newPage();
  await page.goto('https://www.saucedemo.com');
  await page.fill('#user-name', 'standard_user');
  await page.fill('#password', 'secret_sauce');
  await page.click('#login-button');
  await page.click('.inventory_item:has-text("Sauce Labs Backpack") button');
  await page.click('.shopping_cart_link');
  await page.click('button:has-text("Checkout")');
  await page.fill('[data-test="firstName"]', '@@@@');
  await page.fill('[data-test="lastName"]', '####');
  await page.fill('[data-test="postalCode"]', '%^&*');
  await page.click('[data-test="continue"]');
  console.log('url', page.url());
  const error = await page.$('h3[data-test="error"]');
  if (error) console.log('errorText', await error.innerText());
  else console.log('no error');
  await browser.close();
})();